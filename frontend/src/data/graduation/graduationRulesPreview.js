export function preferredGraduationDepartmentName(profileDepartment, departments) {
  const raw = String(profileDepartment || '').trim()
  if (!raw) return departments[0] || ''
  const exact = departments.find((dept) => dept === raw)
  if (exact) return exact
  for (const [full, short] of GRADUATION_DEPARTMENT_ALIASES) {
    if (raw.includes(full) && departments.includes(short)) return short
  }
  const compactRaw = raw.replace(/[\s()（）學系科組班]/g, '')
  return departments.find((dept) => compactRaw.includes(String(dept).replace(/[\s()（）學系科組班]/g, '')) || raw.includes(dept)) || departments[0] || ''
}

export function matchedGraduationRuleForProfile(profile) {
  const preferredYear = Number(profile?.admissionYear) || 0
  const yearRules = GRADUATION_RULE_PREVIEW.filter((rule) => Number(rule.entryYear) === preferredYear)
  const pool = yearRules.length ? yearRules : GRADUATION_RULE_PREVIEW
  const departments = Array.from(new Set(pool.map((rule) => rule.department))).sort((a, b) => a.localeCompare(b, 'zh-Hant'))
  const preferredDept = preferredGraduationDepartmentName(profile?.department, departments)
  return pool.find((rule) => rule.department === preferredDept) || null
}

export function graduationCoverageSummary() {
  const years = [113, 114, 115]
  const allDepartments = Array.from(new Set(GRADUATION_RULE_PREVIEW.map((rule) => rule.department))).sort((a, b) => a.localeCompare(b, 'zh-Hant'))
  return years.map((year) => {
    const present = new Set(GRADUATION_RULE_PREVIEW.filter((rule) => Number(rule.entryYear) === year).map((rule) => rule.department))
    const missing = allDepartments.filter((dept) => !present.has(dept))
    const invalidTotals = GRADUATION_RULE_PREVIEW.filter((rule) => Number(rule.entryYear) === year).filter((rule) => {
      const sum = (rule.categories || []).reduce((total, item) => total + Number(item.required || 0), 0)
      return Number(rule.totalCredits || 0) !== sum
    })
    return { year, total: allDepartments.length, ready: present.size, missing, invalidTotals }
  })
}

